import turtle
turtle.speed()

def square():
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)


def traingle():
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(60)
    turtle.forward(70.71)
    turtle.left(72)
    turtle.forward(60.71)

square()
traingle()
